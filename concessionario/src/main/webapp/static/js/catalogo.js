var slideIndex = 1;
showDivsMercedes(slideIndex);

function plusDivsMercedes(n) {
	showDivsMercedes(slideIndex += n);
}

function showDivsMercedes(n) {
	var i;
	var x = document.getElementsByClassName("mySlidesMercedes");
	if (n > x.length) { slideIndex = 1 }
	if (n < 1) { slideIndex = x.length }
	for (i = 0; i < x.length; i++) {
		x[i].style.display = "none";
	}
	x[slideIndex - 1].style.display = "flex";
}





